/*
*********************************************************************************************************
*                                     Line_Interpolation
*
* Description : 线性插值，进行数值变换
*
* Arguments   : None.
*
* Returns     : None.
*********************************************************************************************************
*/


uint16 Line_Interpolation(uint16 x,uint16 Input_min,uint16 Input_max,uint16 Output_min,uint16 Output_max)                                          // x?AD????
{  
//   int i;
   if(x < Input_min)  return(Output_min);                          //最小值
   if(x >= Input_max) return(Output_max);                          //最大值
   else                                                            //线性区间
   return (Output_min + (((Output_max- Output_min)/10)*(x - Input_min))/((Input_max -Input_min)/10));//用点斜式线性插值算法

}
