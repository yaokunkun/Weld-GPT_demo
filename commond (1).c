			        Job_Data.MIG_PID_DAC_Preinstall_MIN = 1653 + (Job_Data.MIGSetMotorSpeed*6);//	(1960+140)/12=174非脉冲状态下维弧电流为15A		1A=4.65*12=55.8                                       
//			        Job_Data.MIG_PID_DAC_Preinstall_MIN = 1260 + (Job_Data.MIGSetMotorSpeed*7);//	(1960+140)/12=174非脉冲状态下维弧电流为15A		1A=4.65*12=55.8                                       
					break; 	
					case 3:                                                             // 1.6  截流设置为800A
			        Job_Data.MIG_PID_DAC_Preinstall_MIN = 1470 + (Job_Data.MIGSetMotorSpeed*5);//	(1700+120)/10=182非脉冲状态下维弧电流为17A	1A=4.65*10=46.5	                                     
