		Job_Data.MIGSetPluseArcTime    =  PluseArcTime[Job_Data.MIGMaterialMode][Job_Data.MIGMaterialDia][j];              //脉冲燃弧时间
		Job_Data.MIGSetPluseBaseTime   =  Job_Data.MIGSetPluseCycleTime - Job_Data.MIGSetPlusePeakTime;                    //脉冲基值时间		
		Job_Data.MIGSinglePluseDuty    =  (Job_Data.MIGSetPlusePeakTime*100)/Job_Data.MIGSetPluseCycleTime;                //占空比

// 脉冲或一元化状态下根据送丝速度、焊丝直径计算设定电压 设定电流//	 
