/*
*********************************************************************************************************
*                                           SystemDataChang()
*
* Description : 数据转换
*
* Argument(s) : databuff: 需要转换的数据      model: 显示,调整++,调整--   
*                datamax: 调整限制最大值    datamin: 调整限制最小值*
* Return(s)   : none.
*
* Caller(s)   : none.
*
* Note(s)     : none.
*********************************************************************************************************
*/

uint16  SystemDataChang  (uint16 databuff, uint16 model, uint16 datamin, uint16 datamax)
{
  uint16 temp;

  temp = databuff;
  switch (model)  {
    case KEY_DATA_LCM_MSG:                                        // 数据显示                                 
	   ;
       break;
    case KEY_DATA_DOWN_MSG:                                       // 数据调整--                       
	   if((Job_Data.FgRoughMachiningEn == TRUE)||(Job_Data.FgRoughMachiningEn_V == TRUE))// 粗调整数据按键 
	   {
	     if (temp > (datamin + 10))  temp = temp - 10;
	     else temp = datamin;
	   } 
	   else
	   {
	     if (temp > datamin) temp--;
	   }
	   if(temp < datamin)  temp = datamin ;
	   if(temp > datamax)  temp = datamax ;
       break;
    case KEY_DATA_UP_MSG:                                         // 数据调整++                                            
	   if((Job_Data.FgRoughMachiningEn == TRUE)||(Job_Data.FgRoughMachiningEn_V == TRUE))// 粗调整数据按键 
	   {
		 if (temp < (datamax - 10))  temp = temp + 10;
		 else temp = datamax;
	   } 
	   else 
	   {
	     if (temp < datamax) temp++;
	   }
	   if(temp < datamin)  temp = datamin ;
	   if(temp > datamax)  temp = datamax ;
       break;
	default:
       break;
  }
  return temp;
}  
                 
